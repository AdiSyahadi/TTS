def draw_boxes(image, bounds, color='yellow',width=2): # function to draw bounding boxes around the text
    draw=ImageDraw.Draw(image)
    for bound in bounds:
        p0, p1, p2, p3 = bound[0]
        draw.line([*p0, *p1, *p2, *p3, *p0], fill=color, width=width)
    return image


